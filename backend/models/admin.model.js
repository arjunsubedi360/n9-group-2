import mangoose from "mongoose";
const adminSchema = new mangoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      default: "admin",
    },
  },
  {
    timestamps: true,
  }
);
const Admin = mangoose.model("Admin", adminSchema);
export default Admin;
